numero1 = 10
numero2 = 20

# +

print("la suma es:{}".format(numero1 + numero2))

# -
print("la resta es:{}".format(numero1 - numero2))

# *
print("la multiplicacion es:{}".format(numero1 * numero2))

# /
print("la division es:{}".format(numero1 / numero2))

# % MODULO
print("el modulo es:{}".format(numero1 % numero2))

# // COCIENTE
print("el cociente es:{}".format(numero1 // numero2))